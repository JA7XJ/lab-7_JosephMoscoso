    private void bt_registrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_registrarMouseClicked
        // TODO add your handling code here:
        try {
            if ("".equals(reg_nombre.getText()) || (Integer) sp_edad.getValue() < 18 || "".equals(reg_usuario.getText()) || "".equals(reg_contrase単a.getText()) || "".equals(ta_descripcion.getText()) || "".equals(ta_intereses.getText())) {
                JOptionPane.showMessageDialog(jd_registrar, "Lo siento, campo vacio o edad menor que 18");
            } else {
                String[] intereses = ta_intereses.getText().split(",");
                if (intereses.length >= 9) {
                    String sexo = "", tipo = "";
                    if (bt_m.isSelected()) {
                        sexo = "Masculino";
                    } else {
                        sexo = "Femenino";
                    }
                    if (bg_si.isSelected()) {
                        tipo = "Premium";
                    } else {
                        tipo = "No Premium";
                    }
                    ArrayList tempo = new ArrayList();
                    for (int i = 0; i < intereses.length; i++) {
                        tempo.add(intereses[i]);
                    }
                    Usuarios u = new Usuarios(reg_nombre.getText(), (Integer) sp_edad.getValue(), sexo, reg_usuario.getText(), reg_contrase単a.getText(), ta_descripcion.getText(), tipo);
                    u.setIntereses(tempo);
                    administrarUsuarios ap = new administrarUsuarios("./Usuarios.ella");
                    ap.cargarArchivo();
                    ap.setListaUsuario(u);
                    ap.escribirArchivo();
                    JOptionPane.showMessageDialog(jd_registrar, "Usuario registrado con exito");
                    reg_nombre.setText("");
                    reg_usuario.setText("");
                    reg_contrase単a.setText("");
                    ta_descripcion.setText("");
                    ta_intereses.setText("");
                    jd_registrar.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(jd_registrar, "Lo siento, faltan mas intereses y/o espacios");
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_bt_registrarMouseClicked

    private void bt_iniciarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_iniciarMouseClicked
        // TODO add your handling code here:
        try {
            administrarUsuarios ap = new administrarUsuarios("./Usuarios.ella");
            ap.cargarArchivo();
            for (Usuarios t : ap.getListaUsuarios()) {
                if (tf_iu.getText().equals(t.getUsuario()) && tf_ip.getText().equals(t.getContrase単a())) {
                    mostrarn.setText(t.getNombre());
                    mostrare.setText(Integer.toString(t.getEdad()));
                    mostrars.setText(t.getSexo());
                    mostraru.setText(t.getUsuario());
                    mostrarc.setText(t.getContrase単a());
                    mostrard.setText(t.getDescripcion());
                    mostrari.setText(t.getIntereses().toString());
                    if (t.getTipo().equals("Premium")) {
                        jPanel5.setEnabled(true);
                        mostrart.setText(t.getTipo());
                    } else {
                        jPanel5.setEnabled(false);
                        mostrart.setText(t.getTipo());
                    }
                    JOptionPane.showMessageDialog(this, "Inicio exitoso");
                    jd_usuario.setModal(true);
                    jd_usuario.pack();
                    jd_usuario.setLocationRelativeTo(this);
                    jd_usuario.setVisible(true);
                }
            }
            ap.escribirArchivo();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_bt_iniciarMouseClicked

